console.log("----------------------------- Operators -----------------------------")

let a =10
let b=5
console.log("----------------------------- Arithmatic Operators -----------------------------")

console.log("a + b = ",a+b);
console.log("a - b = ",a-b);
console.log("a * b = ",a*b);
console.log("a / b = ",a/b);
console.log("a % b = ",a%b);
console.log("a ** b = ",a**b);

console.log("----------------------------- Unary Operators -----------------------------")
// Increament++/Decreament--

// post increament
a++ //print n+1
console.log("a= ",a); // print n+1

// pre increament
let zz=10
console.log("a= ",++zz); // print n+1

// post decreament
b-- //print n+1
console.log("a= ",b); // print n-1

// pre decreament
let zx=10
console.log("a= ",--zx); // print n+1

console.log("----------------------------- Assign Operators -----------------------------")
//  = ,+= ,-= ,*= ,%= ,**=
let assignvalue=5
assignvalue+=5
console.log("a+= ",assignvalue);

let x=10
